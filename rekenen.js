var Value1 = 3;
var Value2 = 4;
var Result;

Result=Value1 + Value2; //result=3+4=7
alert("3+4=" + Result);

Result=Value1 - Value2; //result=3-4=-1
alert("3-4=" + Result);

Result=Value1 * Value2; //result=3*4=12
alert("3*4=" + Result);

Result=Value1 / Value2; //result=3/4=0.75
alert("3/4=" + Result);

Result=Value2 % Value1; //result=4 modulo 3=1
alert("3 modulo 4=" + Result);

Result=14 % 3; //result=14 modulo 3=2
alert("14 modulo 3=" + Result);

Result=Value1++; //Value1=Result+1
alert(Result + "+1=" + Value1);

Result=Value1--; //Value1=Result-1
alert(Result + "-1=" + Value1);